export const getStackById = (id: string): Stack | undefined =>
  stacks.find(s => s.id === id);

export const getStacksByCategory = (category: string): Stack[] =>
  stacks.filter(s => s.category === category);

export const getStacksByCreator = (userId: string): Stack[] =>
  stacks.filter(s => s.creator.id === userId);

export const getStacksByAtom = (atomId: string): Stack[] =>
  stacks
    .filter(s => s.items.some(item => item.atom.id === atomId))
    .sort((a, b) => new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime());
